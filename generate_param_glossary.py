# ============================================================
# 写入 Excel
# ============================================================
def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    with pd.ExcelWriter(XLSX_PATH, engine="openpyxl") as writer:
        for sheet_name, rows in SHEET_DATA.items():
            df = pd.DataFrame(rows)
            df.to_excel(writer, sheet_name=sheet_name, index=False)

        wb = writer.book
        for sn in wb.sheetnames:
            ws = wb[sn]
            for col_cells in ws.columns:
                max_len = max(
                    (len(str(c.value)) if c.value is not None else 0
                     for c in col_cells), default=8)
                w = min(max(10, max_len + 2), 70)
                ws.column_dimensions[col_cells[0].column_letter].width = w
            ws.freeze_panes = "A2"

            # 标题行加粗
            from openpyxl.styles import Font, PatternFill, Alignment
            title_fill = PatternFill("solid", fgColor="4472C4")
            title_font = Font(color="FFFFFF", bold=True, size=11)
            for cell in ws[1]:
                cell.fill = title_fill
                cell.font = title_font
                cell.alignment = Alignment(horizontal="center", vertical="center")

            # 数据行增加高度
            for row_idx in range(2, ws.max_row + 1):
                ws.row_dimensions[row_idx].height = 28

            # 全部居中 + 自动换行
            for row in ws.iter_rows(min_row=2, max_row=ws.max_row):
                for cell in row:
                    cell.alignment = Alignment(horizontal="left", vertical="center", wrap_text=True)

    print(f"[OK] 已生成: {XLSX_PATH}")
    print(f"\n包含 {len(SHEET_DATA)} 个 Sheet:")
    for k in SHEET_DATA:
        print(f"  {k}: {len(SHEET_DATA[k])} 条参数")


if __name__ == "__main__":
    main()
